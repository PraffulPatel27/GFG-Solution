class Solution
{
    public:
    Node *compute(Node *head)
    {
        // your code goes here
          Node*temp=head;
        Node*curr=NULL;
        while(temp!=NULL && temp->next!=NULL){
            if(temp->data<temp->next->data){  
                temp->data=temp->next->data; //Exchanging temp with temp->next
                curr=temp->next;    //storing temp->next  
                temp->next=temp->next->next; //for deleting removing && creating link
                delete(curr);   //deleting  temp->next which stored in temp->next
                temp=head;
            }
            else{
                temp=temp->next;
            }
        }
        return head;
    }
    
};
   


