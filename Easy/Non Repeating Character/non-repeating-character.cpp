class Solution
{
    public:
    //Function to find the first non-repeating character in a string.
    char nonrepeatingCharacter(string S)
    {
       //Your code here
    char c = '$';
       unordered_map<char,int> mp;
       
       for (auto d : S)
         mp[d]++;
         
       for (auto ch : S)
       {
           if (mp[ch] == 1)
             return ch;
       }
       
       return c;
    }

};

