class Solution{
    public:
    int DivisibleByEight(string s){
        //code here
        int n = s.length();
        string tp = s.substr (n - min(3, n), min(3, n));
        int num = stoi(tp);
        
        return (num % 8 == 0) ? 1 : -1;
    }
};

